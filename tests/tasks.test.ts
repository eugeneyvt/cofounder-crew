async function initGitRepo(dir: string): Promise<void> {
  await execFileAsync("git", ["init"], { cwd: dir });
  await execFileAsync("git", ["config", "user.email", "test@example.com"], { cwd: dir });
  await execFileAsync("git", ["config", "user.name", "Cofounder Test"], { cwd: dir });
  await writeFile(path.join(dir, "tracked.txt"), "clean\n", "utf8");
  await execFileAsync("git", ["add", "tracked.txt"], { cwd: dir });
  await execFileAsync("git", ["commit", "-m", "initial"], { cwd: dir });
}

async function commitAll(dir: string, message: string): Promise<void> {
  await execFileAsync("git", ["add", "."], { cwd: dir });
  await execFileAsync("git", ["commit", "-m", message], { cwd: dir });
}

function createTaskInput(runtime: Awaited<ReturnType<typeof prepareMemberRuntime>>, task: string) {
  return {
    caller: "primary",
    assignee: runtime.member.id,
    task,
    mode: "run" as const,
    member_home_path: runtime.member_home_path,
    member_prompt_path: runtime.member_prompt_path,
    member_settings_path: runtime.member_settings_path,
    member_effective_config_path: runtime.member_effective_config_path,
    member_codex_config_path: runtime.member_codex_config_path,
    work_mode: runtime.settings.write?.mode ?? "direct"
  };
}

async function pollTask(dir: string, taskId: string, predicate: (task: Awaited<ReturnType<typeof getTask>>) => boolean) {
  for (let attempt = 0; attempt < 40; attempt += 1) {
    const task = await getTask(taskId, dir);
    if (predicate(task)) {
      return task;
    }
    await new Promise((resolve) => setTimeout(resolve, 100));
  }
  throw new Error(`Timed out waiting for task ${taskId}`);
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
