test("interrupt cancels a running task and resumes the Codex session", async () => {
  const dir = await mkdtemp(path.join(os.tmpdir(), "cofounder-interrupt-"));
  const fakeBin = await mkdtemp(path.join(os.tmpdir(), "cofounder-interrupt-bin-"));
  const originalPath = process.env.PATH;

  try {
    const fakeCodexPath = path.join(fakeBin, "codex");
    await writeFile(fakeCodexPath, fakeCodexScript(), "utf8");
    await chmod(fakeCodexPath, 0o755);
    process.env.PATH = `${fakeBin}${path.delimiter}${originalPath ?? ""}`;

    await initProject(dir);
    const runningTask = await delegateMember("backend", "stay running until interrupted", {
      caller: "primary",
      startDir: dir
    });
    const taskWithSession = await pollTask(dir, runningTask.id, (task) => Boolean(task.codex_session_id));

    const resumedTask = await interruptTask(taskWithSession.id, "finish after interrupt", dir);
    assert.equal(resumedTask.interrupted_task_id, taskWithSession.id);
    assert.equal(resumedTask.codex_resume_session_id, "fake-session-123");

    const cancelledTask = await getTask(taskWithSession.id, dir);
    assert.equal(cancelledTask.status, "cancelled");

    const completedResume = await pollTask(dir, resumedTask.id, (task) => task.status === "succeeded");
    assert.equal(completedResume.status, "succeeded");
    assert.equal(completedResume.codex_session_id, "fake-session-123");

    const result = await readFile(path.join(dir, completedResume.result_path), "utf8");
    assert.match(result, /fake resumed result/);
    const events = await readFile(path.join(dir, completedResume.events_path), "utf8");
    assert.match(events, /codex -a never exec resume/);
    assert.doesNotMatch(events, /codex exec resume .* -a never/);
  } finally {
    process.env.PATH = originalPath;
    await rm(dir, { recursive: true, force: true });
    await rm(fakeBin, { recursive: true, force: true });
  }
});

